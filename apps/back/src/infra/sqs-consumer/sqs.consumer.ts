import type { Message } from '@aws-sdk/client-sqs'
import { Inject, Injectable, Logger } from '@nestjs/common'
import { back } from 'messages'
import { SqsMessageHandler } from 'sqs'
import { MS_NAME, PUBSUB } from '#constants.js'
import { isAppError, PubSub } from 'utils'

@Injectable()
export class SQSConsumer {
  private logger = new Logger(SQSConsumer.name)

  constructor(
    @Inject(PUBSUB) private readonly pubsub: PubSub<back.BackEvent>,
  ) {}

  @SqsMessageHandler('back', false)
  public async handleMessage(message: Message) {
    if (message.Body) {
      try {
        const body = JSON.parse(message.Body)
        const data: unknown = JSON.parse(body.Message)
        if (back.isBackEvent(data)) {
          const messageAttributes:
            | Record<string, { Type: 'String'; Value: 'string' }>
            | undefined = body.MessageAttributes
          // Note: I should publish into the internal queue only the message
          // generated by other microservices
          if (messageAttributes?.Sender?.Value === (MS_NAME as string)) {
            this.logger.debug(`⛔️ stopping ${data.type} propagation`)
            return
          }
          this.logger.debug(`🚀 Publishing ${data.type} to internal queue`)
          await this.pubsub.publish(data).toPromise()
        } else {
          this.logger.log(`❌ unhandled message: ${(data as any).type}`)
        }
      } catch (err) {
        this.logger.warn(
          `❌ failed to handle message: ${isAppError(err) ? err.message : err}`,
        )
        throw err
      }
    }
  }
}
