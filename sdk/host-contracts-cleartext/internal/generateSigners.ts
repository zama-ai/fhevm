import { mkdirSync, writeFileSync } from 'node:fs';
import { dirname, join, resolve } from 'node:path';
import { fileURLToPath, pathToFileURL } from 'node:url';
import { toHex } from 'viem';
import { mnemonicToAccount } from 'viem/accounts';
import { format, resolveConfig } from 'prettier';

export const PACKAGE_ROOT = join(dirname(fileURLToPath(import.meta.url)), '..');

// Well-known FHEVM test mnemonic. Kept in sync with FHEVM_MNEMONIC in ts/constants.ts.
const FHEVM_MNEMONIC = 'test test test test test test test future home engine virtual motion';

// Size of each derived signer pool. The runtime slices the first N it needs; this is the pool the SDK
// ships. Must match SDK_NUM_SIGNERS in ts/constants.ts (which asserts the loaded pool has this length).
const DEFAULT_SIGNER_COUNT = 20;

export type SignerSet = {
  /** Output module written under `ts/signers/`. */
  readonly fileName: string;
  /** Exported const holding the derived private keys. */
  readonly pkConstName: string;
  /** Exported const holding the derived checksummed addresses. */
  readonly addressConstName: string;
  /** BIP-39 mnemonic to derive from. */
  readonly mnemonic: string;
  /** HD path prefix; the signer index is appended (e.g. `m/44'/60'/0'/3/`). */
  readonly path: string;
  /** Number of signers to derive, starting at index 0. */
  readonly count: number;
};

// One entry per generated module. `path` mirrors the DEFAULT_*_MNEMONIC_PATH constants in ts/constants.ts.
export const SIGNER_SETS: readonly SignerSet[] = [
  {
    fileName: 'defaultCoprocessorSigners.ts',
    pkConstName: 'DEFAULT_COPROCESSOR_PK',
    addressConstName: 'DEFAULT_COPROCESSOR_ADDRESSES',
    mnemonic: FHEVM_MNEMONIC,
    path: "m/44'/60'/0'/2/",
    count: DEFAULT_SIGNER_COUNT,
  },
  {
    fileName: 'defaultKmsSigners.ts',
    pkConstName: 'DEFAULT_KMS_NODE_PK',
    addressConstName: 'DEFAULT_KMS_NODE_ADDRESSES',
    mnemonic: FHEVM_MNEMONIC,
    path: "m/44'/60'/0'/3/",
    count: DEFAULT_SIGNER_COUNT,
  },
  {
    fileName: 'defaultKmsTxSenderSigners.ts',
    pkConstName: 'DEFAULT_KMS_NODE_TX_SENDER_PK',
    addressConstName: 'DEFAULT_KMS_NODE_TX_SENDER_ADDRESSES',
    mnemonic: FHEVM_MNEMONIC,
    path: "m/44'/60'/0'/4/",
    count: DEFAULT_SIGNER_COUNT,
  },
];

type DerivedSigner = { readonly privateKey: `0x${string}`; readonly address: string };

/** Derive `set.count` signers from `set.mnemonic` at `${set.path}<i>`, i in [0, count). */
export function deriveSigners(set: SignerSet): DerivedSigner[] {
  const signers: DerivedSigner[] = [];
  for (let i = 0; i < set.count; ++i) {
    const account = mnemonicToAccount(set.mnemonic, { path: `${set.path}${i}` as `m/44'/60'/${string}` });
    const privateKey = account.getHdKey().privateKey;
    if (privateKey === null) {
      throw new Error(`Could not derive private key for ${set.fileName} at index ${i}`);
    }
    signers.push({ privateKey: toHex(privateKey), address: account.address });
  }

  return signers;
}

function renderModule(set: SignerSet, signers: readonly DerivedSigner[]): string {
  const asArray = (values: readonly string[]): string => values.map((v) => `  '${v}',`).join('\n');

  return `// This file is generated by internal/generateSigners.ts. Do not edit manually.
//
// Equivalent to, for each index i in [0, ${set.count}):
// cast wallet private-key \\
//  --mnemonic "${set.mnemonic}" \\
//  --mnemonic-derivation-path "${set.path}<i>"

export const ${set.pkConstName} = [
${asArray(signers.map((s) => s.privateKey))}
];

export const ${set.addressConstName} = [
${asArray(signers.map((s) => s.address))}
];
`;
}

async function writeTypeScript(path: string, source: string): Promise<void> {
  writeFileSync(
    path,
    await format(source, { ...((await resolveConfig(path)) ?? {}), filepath: path, parser: 'typescript' }),
  );
}

export async function main(): Promise<void> {
  const signersDir = join(PACKAGE_ROOT, 'ts', 'signers');
  mkdirSync(signersDir, { recursive: true });

  for (const set of SIGNER_SETS) {
    const signers = deriveSigners(set);
    await writeTypeScript(join(signersDir, set.fileName), renderModule(set, signers));
  }
}

if (process.argv[1] !== undefined && import.meta.url === pathToFileURL(resolve(process.argv[1])).href) {
  await main();
}
