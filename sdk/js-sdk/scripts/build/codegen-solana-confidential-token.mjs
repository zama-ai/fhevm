import {
  cpSync,
  existsSync,
  mkdtempSync,
  readFileSync,
  readdirSync,
  realpathSync,
  rmSync,
  statSync,
  writeFileSync,
} from 'node:fs';
import { dirname, resolve } from 'node:path';
import { tmpdir } from 'node:os';
import { fileURLToPath } from 'node:url';

import { rootNodeFromAnchor } from '@codama/nodes-from-anchor';
import { renderVisitor } from '@codama/renderers-js';
import { createFromRoot, deleteNodesVisitor } from 'codama';
import { format, resolveConfig } from 'prettier';

const sdkRoot = fileURLToPath(new URL('../..', import.meta.url));
const idlPath = fileURLToPath(
  new URL('../../../../coprocessor/fhevm-engine/host-listener/idl/confidential_token.json', import.meta.url),
);
const generatedPath = `${sdkRoot}/src/solana/internal/generated/confidentialToken`;
const temporaryRoot = mkdtempSync(`${tmpdir()}/fhevm-codama-`);
const temporaryGeneratedPath = `${temporaryRoot}/generated`;
const check = process.argv.includes('--check');

function snapshot(path) {
  if (!existsSync(path)) return new Map();
  path = realpathSync(path);
  return new Map(
    readdirSync(path, { recursive: true, withFileTypes: true })
      .filter((entry) => entry.isFile())
      .map((entry) => [
        `${entry.parentPath}/${entry.name}`.slice(path.length + 1),
        readFileSync(`${entry.parentPath}/${entry.name}`, 'utf8'),
      ])
      .sort(([left], [right]) => left.localeCompare(right)),
  );
}

const before = check ? snapshot(generatedPath) : undefined;
const prettierOptions = (await resolveConfig(`${sdkRoot}/generated.ts`)) ?? {};
const anchorIdl = JSON.parse(readFileSync(idlPath, 'utf8'));
const codama = createFromRoot(rootNodeFromAnchor(anchorIdl));
const program = codama.getRoot().program;
const zamaHostProgramAddress = anchorIdl.instructions
  .find(({ name }) => name === 'confidential_transfer')
  ?.accounts.find(({ name }) => name === 'zama_program')?.address;
if (typeof zamaHostProgramAddress !== 'string') {
  throw new Error('confidential_transfer.zama_program.address is missing from the committed IDL');
}

const keep = {
  instructions: new Set(['confidentialTransfer']),
  definedTypes: new Set(['coprocessorInputAttestation']),
  pdas: new Set(['computeSigner']),
};
for (const [kind, names] of Object.entries(keep)) {
  const present = new Set(program[kind].map((node) => node.name));
  for (const name of names) {
    if (!present.has(name)) throw new Error(`Required Codama ${kind} node is missing: ${name}`);
  }
}

const selectors = [
  ...program.instructions
    .filter(({ name }) => !keep.instructions.has(name))
    .map(({ name }) => `[instructionNode]${name}`),
  ...program.accounts.map(({ name }) => `[accountNode]${name}`),
  ...program.definedTypes
    .filter(({ name }) => !keep.definedTypes.has(name))
    .map(({ name }) => `[definedTypeNode]${name}`),
  ...program.pdas.filter(({ name }) => !keep.pdas.has(name)).map(({ name }) => `[pdaNode]${name}`),
  ...program.errors.map(({ name }) => `[errorNode]${name}`),
  ...(program.events ?? []).map(({ name }) => `[eventNode]${name}`),
  ...(program.constants ?? []).map(({ name }) => `[constantNode]${name}`),
];
codama.update(deleteNodesVisitor(selectors));
await codama.accept(
  renderVisitor(temporaryRoot, {
    generatedFolder: 'generated',
    kitImportStrategy: 'rootOnly',
    syncPackageJson: false,
  }),
);
writeFileSync(
  `${temporaryGeneratedPath}/programAddress.ts`,
  `/** This code was AUTOGENERATED from the committed confidential-token IDL. */\nimport type { Address } from '@solana/kit';\n\nexport const CONFIDENTIAL_TOKEN_PROGRAM_ADDRESS = '${program.publicKey}' as Address<'${program.publicKey}'>;\nexport const ZAMA_HOST_PROGRAM_ADDRESS = '${zamaHostProgramAddress}' as Address<'${zamaHostProgramAddress}'>;\n`,
);
rmSync(`${temporaryGeneratedPath}/programs`, { force: true, recursive: true });
rmSync(`${temporaryGeneratedPath}/index.ts`, { force: true });

// The SDK builds with NodeNext. Codama renders extensionless relative imports, so make its
// deterministic output executable without hand-editing generated files.
for (const entry of readdirSync(temporaryGeneratedPath, { recursive: true, withFileTypes: true })) {
  if (!entry.isFile() || !entry.name.endsWith('.ts')) continue;
  const file = `${entry.parentPath}/${entry.name}`;
  const source = readFileSync(file, 'utf8')
    .replace(/(['"])\.\.\/programs\1/g, '$1../programAddress$1')
    .replaceAll('@solana/kit/program-client-core', '@solana/program-client-core');
  writeFileSync(
    file,
    await format(
      source.replace(
        /(from\s+['"]|export\s+\*\s+from\s+['"])(\.{1,2}\/[^'"]+)(['"])/g,
        (_, prefix, specifier, suffix) => {
          const target = resolve(dirname(file), specifier);
          const extension = existsSync(target) && statSync(target).isDirectory() ? '/index.js' : '.js';
          return `${prefix}${specifier}${extension}${suffix}`;
        },
      ),
      { ...prettierOptions, parser: 'typescript' },
    ),
  );
}
if (check) {
  const after = snapshot(temporaryGeneratedPath);
  if (JSON.stringify([...before]) !== JSON.stringify([...after])) {
    throw new Error('Generated Solana confidential-token client is stale; run npm run codegen:solana');
  }
} else {
  rmSync(generatedPath, { force: true, recursive: true });
  cpSync(temporaryGeneratedPath, generatedPath, { recursive: true });
}
rmSync(temporaryRoot, { force: true, recursive: true });
