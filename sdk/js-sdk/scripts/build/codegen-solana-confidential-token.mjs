import {
  cpSync,
  existsSync,
  mkdtempSync,
  readFileSync,
  readdirSync,
  realpathSync,
  rmSync,
  statSync,
  writeFileSync,
} from 'node:fs';
import { dirname, resolve } from 'node:path';
import { tmpdir } from 'node:os';
import { fileURLToPath } from 'node:url';

import { rootNodeFromAnchor } from '@codama/nodes-from-anchor';
import { renderVisitor } from '@codama/renderers-js';
import { createFromRoot, deleteNodesVisitor } from 'codama';
import { format, resolveConfig } from 'prettier';

const sdkRoot = fileURLToPath(new URL('../..', import.meta.url));
const check = process.argv.includes('--check');
const prettierOptions = (await resolveConfig(`${sdkRoot}/generated.ts`)) ?? {};

function idlUrl(name) {
  return fileURLToPath(new URL(`../../../../coprocessor/fhevm-engine/host-listener/idl/${name}`, import.meta.url));
}

function snapshot(path) {
  if (!existsSync(path)) return new Map();
  path = realpathSync(path);
  return new Map(
    readdirSync(path, { recursive: true, withFileTypes: true })
      .filter((entry) => entry.isFile())
      .map((entry) => [
        `${entry.parentPath}/${entry.name}`.slice(path.length + 1),
        readFileSync(`${entry.parentPath}/${entry.name}`, 'utf8'),
      ])
      .sort(([left], [right]) => left.localeCompare(right)),
  );
}

// One Codama render per target program. The confidential-token client keeps the two app-facing
// instructions (confidentialTransfer, discloseSecp); the zama-host client keeps only the stateless
// public-decrypt verifier (verifyPublicDecrypt) so callers can build the raw host instruction
// without an on-chain token wrapper. Everything else is pruned so the vendored surface stays tiny.
const targets = [
  {
    idlPath: idlUrl('confidential_token.json'),
    generatedPath: `${sdkRoot}/src/solana/internal/generated/confidentialToken`,
    keep: {
      // The two app-facing instructions plus the four the confidential-vault demo builds directly:
      // confidentialTransferFromValue and wrapUsdc (the on-chain from-value/wrap surface, deferred
      // in #1680 and now absorbed), initializeTokenAccount (the vault `claim` flow needs the user
      // payout token account to pre-exist), and initializeMint (the demo seeder mints cUSDC/cShares
      // — fhevm-internal#1760, no hand-rolled instruction bytes). redeemBurnedAmount stays pruned —
      // it is a batcher-internal CPI inside settle, never built by the SDK.
      instructions: new Set([
        'confidentialTransfer',
        'discloseSecp',
        'confidentialTransferFromValue',
        'wrapUsdc',
        'initializeTokenAccount',
        'initializeMint',
      ]),
      // coprocessorInputAttestation backs confidentialTransfer/wrapUsdc; mmrInclusionProof is the
      // disclose_secp `proof` argument (the flat leaf_index/siblings pair was folded into this
      // Anchor-native struct by #3252/#3248 — keeping it lets the regenerated builder resolve).
      definedTypes: new Set(['coprocessorInputAttestation', 'mmrInclusionProof']),
      // computeSigner defaults confidentialTransfer; the rest default the newly-kept builders
      // (wrapUsdc → vaultAuthority/totalSupplyAuthority, initializeTokenAccount → tokenAccount).
      pdas: new Set(['computeSigner', 'vaultAuthority', 'totalSupplyAuthority', 'tokenAccount']),
    },
    programAddress(program, anchorIdl) {
      const zamaHostProgramAddress = anchorIdl.instructions
        .find(({ name }) => name === 'confidential_transfer')
        ?.accounts.find(({ name }) => name === 'zama_program')?.address;
      if (typeof zamaHostProgramAddress !== 'string') {
        throw new Error('confidential_transfer.zama_program.address is missing from the committed IDL');
      }
      return (
        `/** This code was AUTOGENERATED from the committed confidential-token IDL. */\n` +
        `import type { Address } from '@solana/kit';\n\n` +
        `export const CONFIDENTIAL_TOKEN_PROGRAM_ADDRESS = '${program.publicKey}' as Address<'${program.publicKey}'>;\n` +
        `export const ZAMA_HOST_PROGRAM_ADDRESS = '${zamaHostProgramAddress}' as Address<'${zamaHostProgramAddress}'>;\n`
      );
    },
  },
  {
    idlPath: idlUrl('zama_host.json'),
    generatedPath: `${sdkRoot}/src/solana/internal/generated/zamaHost`,
    keep: {
      instructions: new Set(['verifyPublicDecrypt']),
      definedTypes: new Set(),
      // verifyPublicDecrypt defaults its host_config account to the same-program host-config PDA, so
      // the generated builder imports findHostConfigPda; keep that PDA node so the import resolves.
      pdas: new Set(['hostConfig']),
    },
    programAddress(program) {
      return (
        `/** This code was AUTOGENERATED from the committed zama-host IDL. */\n` +
        `import type { Address } from '@solana/kit';\n\n` +
        `export const ZAMA_HOST_PROGRAM_ADDRESS = '${program.publicKey}' as Address<'${program.publicKey}'>;\n`
      );
    },
  },
  {
    // The confidential-batcher client for the confidential-vault demo lives under the isolated
    // vault module (src/solana/vault), not the shared solana/internal root, so the demo surface
    // never leaks into the SDK's normal Solana paths. Keeps every batch-lifecycle instruction, the
    // defined types their args reference, and the Batch/Batcher/JoinRecord account decoders the
    // vault module's on-chain reads use (fhevm-internal#1760 reads.ts — generated decoders, never
    // hand-rolled borsh).
    idlPath: idlUrl('confidential_batcher.json'),
    generatedPath: `${sdkRoot}/src/solana/vault/internal/generated/confidentialBatcher`,
    keep: {
      instructions: new Set(['initializeBatcher', 'openBatch', 'join', 'quit', 'dispatch', 'settle', 'claim']),
      // Anchor inlines the CoprocessorInputAttestation (join) and MmrInclusionProof (settle) structs
      // directly into the instruction data. Keep the initializeBatcher direction enum plus batchStatus
      // — the Batch account decoder references it; confidentialMint/vault stay pruned (account-only).
      definedTypes: new Set(['batchDirection', 'batchStatus']),
      // Keep the three account decoders the vault-module reads consume: Batcher (direction + mints +
      // vault + min_batch_age_slots + next_batch_index), Batch (status + opened_slot + join_count +
      // burned_total_handle), and JoinRecord (per-user claim state).
      accounts: new Set(['batch', 'batcher', 'joinRecord']),
      // Keep every batch-scoped PDA the generated builders resolve (batchAuthority, joinRecord, and
      // the two plain-SPL accounts): all seed only on the batch (and user), never on the batcher's
      // next_batch_index, so their find* helpers are pure derivations. The `batch` PDA itself seeds
      // on batcher.next_batch_index and Codama does not emit a resolver for it — callers pass it in.
      pdas: new Set(['batchAuthority', 'joinRecord', 'batchJoinUnderlying', 'batchPayoutUnderlying']),
    },
    programAddress(program) {
      return (
        `/** This code was AUTOGENERATED from the committed confidential-batcher IDL. */\n` +
        `import type { Address } from '@solana/kit';\n\n` +
        `export const CONFIDENTIAL_BATCHER_PROGRAM_ADDRESS = '${program.publicKey}' as Address<'${program.publicKey}'>;\n`
      );
    },
  },
  {
    // The demo-vault client, also isolated under the vault module. The confidential-vault demo
    // seeder is the only SDK caller (fhevm-internal#1760), so only initializeVault is kept — it
    // creates the public share-mint vault the two batchers front. Its three PDAs (vaultAuthority,
    // shareMint, vaultTokenAccount) all seed on the vault account alone, so their find* helpers are
    // pure derivations; every account decoder is pruned (the seeder derives, never reads, the vault).
    idlPath: idlUrl('demo_vault.json'),
    generatedPath: `${sdkRoot}/src/solana/vault/internal/generated/demoVault`,
    keep: {
      instructions: new Set(['initializeVault']),
      definedTypes: new Set(),
      pdas: new Set(['vaultAuthority', 'shareMint', 'vaultTokenAccount']),
    },
    programAddress(program) {
      return (
        `/** This code was AUTOGENERATED from the committed demo-vault IDL. */\n` +
        `import type { Address } from '@solana/kit';\n\n` +
        `export const DEMO_VAULT_PROGRAM_ADDRESS = '${program.publicKey}' as Address<'${program.publicKey}'>;\n`
      );
    },
  },
];

let stale = false;
for (const target of targets) {
  const before = check ? snapshot(target.generatedPath) : undefined;
  const temporaryRoot = mkdtempSync(`${tmpdir()}/fhevm-codama-`);
  const temporaryGeneratedPath = `${temporaryRoot}/generated`;

  const anchorIdl = JSON.parse(readFileSync(target.idlPath, 'utf8'));
  const codama = createFromRoot(rootNodeFromAnchor(anchorIdl));
  const program = codama.getRoot().program;

  for (const [kind, names] of Object.entries(target.keep)) {
    const present = new Set(program[kind].map((node) => node.name));
    for (const name of names) {
      if (!present.has(name)) throw new Error(`Required Codama ${kind} node is missing: ${name}`);
    }
  }

  const selectors = [
    ...program.instructions
      .filter(({ name }) => !target.keep.instructions.has(name))
      .map(({ name }) => `[instructionNode]${name}`),
    ...program.accounts
      .filter(({ name }) => !(target.keep.accounts ?? new Set()).has(name))
      .map(({ name }) => `[accountNode]${name}`),
    ...program.definedTypes
      .filter(({ name }) => !target.keep.definedTypes.has(name))
      .map(({ name }) => `[definedTypeNode]${name}`),
    ...program.pdas.filter(({ name }) => !target.keep.pdas.has(name)).map(({ name }) => `[pdaNode]${name}`),
    ...program.errors.map(({ name }) => `[errorNode]${name}`),
    ...(program.events ?? []).map(({ name }) => `[eventNode]${name}`),
    ...(program.constants ?? []).map(({ name }) => `[constantNode]${name}`),
  ];
  codama.update(deleteNodesVisitor(selectors));
  await codama.accept(
    renderVisitor(temporaryRoot, {
      generatedFolder: 'generated',
      kitImportStrategy: 'rootOnly',
      syncPackageJson: false,
    }),
  );
  writeFileSync(`${temporaryGeneratedPath}/programAddress.ts`, target.programAddress(program, anchorIdl));
  rmSync(`${temporaryGeneratedPath}/programs`, { force: true, recursive: true });
  rmSync(`${temporaryGeneratedPath}/index.ts`, { force: true });

  // The SDK builds with NodeNext. Codama renders extensionless relative imports, so make its
  // deterministic output executable without hand-editing generated files.
  for (const entry of readdirSync(temporaryGeneratedPath, { recursive: true, withFileTypes: true })) {
    if (!entry.isFile() || !entry.name.endsWith('.ts')) continue;
    const file = `${entry.parentPath}/${entry.name}`;
    const source = readFileSync(file, 'utf8')
      .replace(/(['"])\.\.\/programs\1/g, '$1../programAddress$1')
      .replaceAll('@solana/kit/program-client-core', '@solana/program-client-core');
    writeFileSync(
      file,
      await format(
        source.replace(
          /(from\s+['"]|export\s+\*\s+from\s+['"])(\.{1,2}\/[^'"]+)(['"])/g,
          (_, prefix, specifier, suffix) => {
            const targetPath = resolve(dirname(file), specifier);
            const extension = existsSync(targetPath) && statSync(targetPath).isDirectory() ? '/index.js' : '.js';
            return `${prefix}${specifier}${extension}${suffix}`;
          },
        ),
        { ...prettierOptions, parser: 'typescript' },
      ),
    );
  }

  if (check) {
    const after = snapshot(temporaryGeneratedPath);
    if (JSON.stringify([...before]) !== JSON.stringify([...after])) stale = true;
  } else {
    rmSync(target.generatedPath, { force: true, recursive: true });
    cpSync(temporaryGeneratedPath, target.generatedPath, { recursive: true });
  }
  rmSync(temporaryRoot, { force: true, recursive: true });
}

if (check && stale) {
  throw new Error('Generated Solana client is stale; run npm run codegen:solana');
}
